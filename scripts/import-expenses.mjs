// One-time import: parse CSV → write src/lib/importedExpenses.js
// Run: node scripts/import-expenses.mjs
import { readFileSync, writeFileSync } from "fs";
import { randomBytes } from "crypto";

const csv = readFileSync("C:/Users/selvaku/Downloads/export_19_04_26_329.csv", "utf8");

const CAT_MAP = {
  "Baby":             "baby",
  "Food":             "food",
  "Gifts for People": "gifts",
  "Grocery":          "grocery",
  "Medical":          "medical",
  "Non Recurring":    "non_recurring",
  "Parents":          "parents",
  "Rent":             "rent",
  "Shopping":         "shopping",
  "Transportation":   "transport",
  "Utilities":        "utilities",
  "Vacation":         "vacation",
  "Weekend Fun":      "weekend_fun",
};

function genId() {
  return randomBytes(6).toString("hex");
}

// Simple CSV parser for quoted fields
function parseCSV(text) {
  const lines = text.split("\n");
  const rows = [];
  for (let i = 1; i < lines.length; i++) { // skip header
    const line = lines[i].trim();
    if (!line) continue;
    // Extract quoted fields
    const fields = [];
    let j = 0;
    while (j < line.length) {
      if (line[j] === '"') {
        j++;
        let field = "";
        while (j < line.length) {
          if (line[j] === '"' && line[j+1] === '"') { field += '"'; j += 2; }
          else if (line[j] === '"') { j++; break; }
          else field += line[j++];
        }
        fields.push(field.trim());
        if (line[j] === ",") j++;
      } else {
        j++;
      }
    }
    if (fields.length >= 4) rows.push(fields);
  }
  return rows;
}

// "Apr 01, 2026 1:36 PM" → "2026-04-01"
function parseDate(str) {
  const d = new Date(str);
  return isNaN(d) ? null : d.toISOString().slice(0, 10);
}

const rows = parseCSV(csv);
const txData = [];

for (const row of rows) {
  const [time, type, amount, category, , notes] = row;
  if (!type?.includes("Expense")) continue;
  const date = parseDate(time);
  if (!date || date < "2026-04-01") continue;
  txData.push({
    id: genId(),
    date,
    amount: parseFloat(amount),
    categoryId: CAT_MAP[category?.trim()] ?? "misc",
    note: (notes || "").replace(/\n/g, " ").trim(),
    person: "Selva",
  });
}

// Write as a JS module so the app can import it once
const out = `// AUTO-GENERATED by scripts/import-expenses.mjs — do not edit
export const IMPORTED_TX = ${JSON.stringify(txData, null, 2)};
`;
writeFileSync("C:/Users/selvaku/Personal/family-finance-app/src/lib/importedExpenses.js", out);
console.log(`Wrote ${txData.length} transactions to src/lib/importedExpenses.js`);
